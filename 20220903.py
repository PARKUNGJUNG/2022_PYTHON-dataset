# Two asterisks ** are used to raise a number to the power of another, 
# which is called exponentiation.


print(2**5)


# You can chain exponentiations together.
# In other words, you can raise a number to a power, 
# which can also be raised to a power.

print(2**3**2)
# = 2⁹

print(9 ** (1 / 2))

print(8 ** (1 / 3))

# //를 사용해 output 하는 경우 몫만 나옴.
print(10 // 4)

# %를 사용해 output 하는 경우 나머지만 나옴.
print(20 % 6)

#Write code to output 4 raised to the 5th power.
print (4 ** 5)

